// Function to find square root
// x: element to find square root
class Solution{
  public:
    long long int floorSqrt(long long int x){
        int n=x;
        int s;
        s=sqrt(n);
        int f;
        f=floor(s);
        return f;
    }
};

// { Driver Code Starts.

int main()
{
	int t;
	cin>>t;
	while(t--)
	{
		long long n;
		cin>>n;
		Solution obj;
		cout << obj.floorSqrt(n) << endl;
	}
    return 0;   
}
