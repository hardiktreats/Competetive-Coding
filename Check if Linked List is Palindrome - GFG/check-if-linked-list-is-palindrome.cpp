class Solution{
  public:
    //Function to check whether the list is palindrome.
    bool isPalindrome(Node *head){
        Node *p=head;
        int l=0;
        while(p){
            l++;
            p=p->next;
        }
        int a[l];
        p=head;
        for(int i=0;i<l;i++){
            a[i]=p->data;
            p=p->next;
        }
        int i=0,j=l-1;
        while(i<j){
            if(a[i]!=a[j])
            return 0;
            i++;
            j--;
        }
        return 1;
    }
};

